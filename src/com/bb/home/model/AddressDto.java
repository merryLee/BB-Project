package com.bb.home.model;

public class AddressDto {
	
	private int pno;
	private String si;
	private String gu;
	private String street;
	private String builno;
	private String builname;
	
	public int getPno() {
		return pno;
	}
	public void setPno(int pno) {
		this.pno = pno;
	}
	public String getSi() {
		return si;
	}
	public void setSi(String si) {
		this.si = si;
	}
	public String getGu() {
		return gu;
	}
	public void setGu(String gu) {
		this.gu = gu;
	}
	public String getStreet() {
		return street;
	}
	public void setStreet(String street) {
		this.street = street;
	}
	public String getBuilno() {
		return builno;
	}
	public void setBuilno(String builno) {
		this.builno = builno;
	}
	public String getBuilname() {
		return builname;
	}
	public void setBuilname(String builname) {
		this.builname = builname;
	}

}
